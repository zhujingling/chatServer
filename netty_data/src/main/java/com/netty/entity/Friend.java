package com.netty.entity;

public class Friend {
	private String friend_pk;
	private String user_number;
	private String friend_number;
	public String getFriend_pk() {
		return friend_pk;
	}
	public void setFriend_pk(String friend_pk) {
		this.friend_pk = friend_pk;
	}
	public String getUser_number() {
		return user_number;
	}
	public void setUser_number(String user_number) {
		this.user_number = user_number;
	}
	public String getFriend_number() {
		return friend_number;
	}
	public void setFriend_number(String friend_number) {
		this.friend_number = friend_number;
	}
	

}
